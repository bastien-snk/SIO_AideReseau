    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        
        String[] ip = AdresseIp.getText().split("\\.");
        if (ip.length != 4 || AdresseIp.getText().length() > 16) {
            JOptionPane.showMessageDialog(null, "Ce n'est pas une adresse IP");
            return;
        }
        
        Integer num = Integer.valueOf(ip[0]);
        if (num <= 127) {
            JOptionPane.showMessageDialog(null, "Classe A");
        } else if (num <= 172) {
            JOptionPane.showMessageDialog(null, "Classe B");
        } else if (num <= 192) {
            JOptionPane.showMessageDialog(null, "Classe C");
        } else {
            JOptionPane.showMessageDialog(null, "Classe D");
        }
        
        JOptionPane.showMessageDialog(null,"IP: " + AdresseIp.getText());        
    }//GEN-LAST:event_jButton1ActionPerformed

